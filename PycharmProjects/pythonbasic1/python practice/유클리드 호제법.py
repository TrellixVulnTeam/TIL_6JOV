# 8 : 8 16 24 32...
#
# 1 2 4 8
#
# 12 : 12 24 ...
#
# 1 2 3 4 6 12
#
# 최소공배수 : 24
#
# 최대공약수 : 4
#
# ​
#
# 8*12/최소공배수 = 최대공약수
#
# 96/24=4
#
# ​
#
# 유클리드 호제법(뺄셈)
#
# 8 12
#
# 8 4
#
# 4 4 => 최대공약수
#
# 8*12/4=24(최소공배수)
#
# ​
#
# 유클리드 호제법(나눗셈)
#
# 8 12
#
# 12 8 몫:1, 나머지:4
#
# 8 4 몫:2, 나머지:0
#
# 나머지가 0이 나오면 연산 종료 -> 젯수 자리의 수가 최대공약수(4)
#
# ​
#
# 13 3 몫:4 나머지:1
#
# 3 1 몫:3 나머지:0 -> 젯수 자리의 수가 최대공약수(1)
